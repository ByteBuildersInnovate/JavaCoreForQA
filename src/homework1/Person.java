package homework1;

public class Person {

    private String name;
    private float overcameAnObstacle;

    public Person(String name, float overcameAnObstacle) {
        this.name = name;
        this.overcameAnObstacle = overcameAnObstacle;
    }

    public String getName() {
        return name;
    }

    public float getOvercameAnObstacle() {
        return overcameAnObstacle;
    }
}
